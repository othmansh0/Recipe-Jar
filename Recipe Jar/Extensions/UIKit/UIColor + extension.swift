extension UIColor {
    /// Initializes and returns a color object using the specified hexadecimal string and opacity.
    /// - Parameters:
    ///   - hex: The hexadecimal string to be converted into a color object. This can be a 3, 4, 6, or 8 digit hex code.
    ///   - opacity: The opacity value of the color object. The default is 1.0 (fully opaque).
    convenience init?(hex: String, opacity: CGFloat = 1.0) {
        var hexSanitized = hex.trimmingCharacters(in: .whitespacesAndNewlines)
        hexSanitized = hexSanitized.replacingOccurrences(of: "#", with: "")

        var hexValue: UInt64 = 0
        Scanner(string: hexSanitized).scanHexInt64(&hexValue)

        let red: CGFloat
        let green: CGFloat
        let blue: CGFloat
        let alpha: CGFloat = opacity

        switch hexSanitized.count {
        case 3: // RGB (12-bit)
            (red, green, blue) = (
                CGFloat((hexValue & 0xF00) >> 8) / 15.0,
                CGFloat((hexValue & 0x0F0) >> 4) / 15.0,
                CGFloat(hexValue & 0x00F) / 15.0
            )
        case 6: // RGB (24-bit)
            (red, green, blue) = (
                CGFloat((hexValue & 0xFF0000) >> 16) / 255.0,
                CGFloat((hexValue & 0x00FF00) >> 8) / 255.0,
                CGFloat(hexValue & 0x0000FF) / 255.0
            )
        default:
            return nil // Invalid hex string
        }

        self.init(red: red, green: green, blue: blue, alpha: alpha)
    }
}
